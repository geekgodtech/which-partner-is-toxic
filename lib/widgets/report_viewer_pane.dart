class _AnalysisReportView extends StatefulWidget {
  final PsychologicalAnalysisReport report;
  final Uint8List? pdfBytes;
  final String? errorMessage;

  const _AnalysisReportView({
    required this.report,
    required this.pdfBytes,
    required this.errorMessage,
  });

  @override
  State<_AnalysisReportView> createState() => _AnalysisReportViewState();
}

class _AnalysisReportViewState extends State<_AnalysisReportView> {
  bool _isPdfPreviewOpen = false;
  bool _debugBlurDisabled = false;

  @override
  Widget build(BuildContext context) {
    final report = widget.report;
    final pdfBytes = widget.pdfBytes;
    final controller = context.watch<ToxicityAnalyzerController>();
    final canAccessFullReport =
        controller.canAccessFullCurrentReport || _debugBlurDisabled;

    return Focus(
      autofocus: true,
      onKeyEvent: (node, event) {
        if (event is KeyDownEvent &&
            event.logicalKey == LogicalKeyboardKey.keyH &&
            HardwareKeyboard.instance.isControlPressed &&
            HardwareKeyboard.instance.isAltPressed) {
          setState(() {
            _debugBlurDisabled = !_debugBlurDisabled;
          });
          ScaffoldMessenger.of(context).showSnackBar(
            SnackBar(
              content: Text(
                _debugBlurDisabled
                    ? 'Debug: Blur disabled'
                    : 'Debug: Blur enabled',
              ),
              duration: const Duration(seconds: 1),
            ),
          );
          return KeyEventResult.handled;
        }
        return KeyEventResult.ignored;
      },
      child: Stack(
        children: [
          Positioned.fill(
            child: SingleChildScrollView(
              padding: const EdgeInsets.fromLTRB(16, 16, 16, 96),
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.stretch,
                children: [
                  _PdfStatusCard(
                    pdfBytes: pdfBytes,
                    errorMessage: widget.errorMessage,
                  ),
                  const SizedBox(height: 16),
                  if (pdfBytes != null) ...[
                    _PdfActionCard(pdfBytes: pdfBytes, controller: controller),
                    const SizedBox(height: 16),
                    _PdfPreviewToggleCard(
                      isOpen: _isPdfPreviewOpen,
                      canAccessFullReport: canAccessFullReport,
                      onPressed: () {
                        if (!canAccessFullReport) {
                          _openMembershipLandingPage(context);
                          return;
                        }
                        setState(() => _isPdfPreviewOpen = !_isPdfPreviewOpen);
                      },
                    ),
                    const SizedBox(height: 8),
                    if (canAccessFullReport)
                      AnimatedCrossFade(
                        firstChild: const SizedBox.shrink(),
                        secondChild: SizedBox(
                          height: 620,
                          child: Card(
                            clipBehavior: Clip.antiAlias,
                            child: PdfPreview(
                              build: (format) async => pdfBytes,
                              canChangeOrientation: false,
                              canChangePageFormat: false,
                              allowSharing: true,
                              allowPrinting: true,
                              pdfFileName: 'relationship_analysis_report.pdf',
                            ),
                          ),
                        ),
                        crossFadeState: _isPdfPreviewOpen
                            ? CrossFadeState.showSecond
                            : CrossFadeState.showFirst,
                        duration: const Duration(milliseconds: 260),
                      ),
                    const SizedBox(height: 16),
                  ],
                  _ReportSectionCard(
                    title: 'Executive Summary',
                    obscureContent: !canAccessFullReport,
                    visiblePreviewHeight: 44,
                    child: Text(report.executiveSummary),
                  ),
                  const SizedBox(height: 16),
                  _LockedReportSectionStack(
                    canAccessFullReport: canAccessFullReport,
                    children: [
                      _ReportSectionCard(
                        title: 'Metric Scores',
                        obscureContent: !canAccessFullReport,
                        visiblePreviewHeight: 28,
                        child: Column(
                          children: report.metricScores.entries
                              .map(
                                (entry) => _MetricScoreRow(
                                  metricName: entry.key,
                                  score: entry.value,
                                ),
                              )
                              .toList(),
                        ),
                      ),
                      const SizedBox(height: 16),
                      _ReportSectionCard(
                        title: 'Contextual Evidence Examples',
                        obscureContent: !canAccessFullReport,
                        visiblePreviewHeight: 28,
                        child: Column(
                          crossAxisAlignment: CrossAxisAlignment.start,
                          children: report.evidenceExamples
                              .map(
                                (example) =>
                                    _EvidenceExampleTile(example: example),
                              )
                              .toList(),
                        ),
                      ),
                      if (report.forensicVerdict.isNotEmpty) ...[
                        const SizedBox(height: 16),
                        _ReportSectionCard(
                          title: 'Neutral Synthesis',
                          obscureContent: !canAccessFullReport,
                          visiblePreviewHeight: 28,
                          child: Text(report.forensicVerdict),
                        ),
                      ],
                      if (report.partnerOneAnalysis.isNotEmpty) ...[
                        const SizedBox(height: 16),
                        _ReportSectionCard(
                          title: 'Analysis of ${report.senderName} (Sender)',
                          obscureContent: !canAccessFullReport,
                          visiblePreviewHeight: 28,
                          child: Text(report.partnerOneAnalysis),
                        ),
                      ],
                      if (report.partnerTwoAnalysis.isNotEmpty) ...[
                        const SizedBox(height: 16),
                        _ReportSectionCard(
                          title:
                              'Analysis of ${report.receiverName} (Receiver)',
                          obscureContent: !canAccessFullReport,
                          visiblePreviewHeight: 28,
                          child: Text(report.partnerTwoAnalysis),
                        ),
                      ],
                      if (report.primaryProblemConclusion.isNotEmpty) ...[
                        const SizedBox(height: 16),
                        _ReportSectionCard(
                          title: 'Final Conclusion',
                          obscureContent: !canAccessFullReport,
                          visiblePreviewHeight: 28,
                          child: Text(report.primaryProblemConclusion),
                        ),
                      ],
                    ],
                  ),
                ],
              ),
            ),
          ),
          if (!canAccessFullReport)
            Center(
              child: Card(
                elevation: 8,
                child: Padding(
                  padding: const EdgeInsets.symmetric(horizontal: 2),
                  child: ElevatedButton.icon(
                    onPressed: () => _openMembershipLandingPage(context),
                    icon: const Icon(Icons.lock_open),
                    label: const Text(
                      'Unlock and show this entire report',
                      textAlign: TextAlign.center,
                    ),
                  ),
                ),
              ),
            ),
        ],
      ),
    );
  }
}

