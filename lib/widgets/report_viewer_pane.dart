class _ObscuredReportContent extends StatefulWidget {
  final Widget child;
  final double visiblePreviewHeight;

  const _ObscuredReportContent({
    required this.child,
    required this.visiblePreviewHeight,
  });

  @override
  State<_ObscuredReportContent> createState() => _ObscuredReportContentState();
}

class _ObscuredReportContentState extends State<_ObscuredReportContent> {
  final GlobalKey _childKey = GlobalKey();
  double? _blurStartPosition;

  @override
  void initState() {
    super.initState();
    WidgetsBinding.instance.addPostFrameCallback((_) {
      _measureChild();
    });
  }

  void _measureChild() {
    final RenderBox? renderBox =
        _childKey.currentContext?.findRenderObject() as RenderBox?;
    if (renderBox != null && mounted) {
      final height = renderBox.size.height;
      setState(() {
        _blurStartPosition = height * 0.25;
      });
    }
  }

  @override
  Widget build(BuildContext context) {
    final colorScheme = Theme.of(context).colorScheme;

    return ClipRect(
      child: Stack(
        children: [
          KeyedSubtree(key: _childKey, child: widget.child),
          if (_blurStartPosition != null)
            Positioned(
              left: 0,
              right: 0,
              top: _blurStartPosition,
              bottom: 0,
              child: ClipRect(
                child: BackdropFilter(
                  filter: ImageFilter.blur(sigmaX: 4, sigmaY: 4),
                  child: Container(
                    color: colorScheme.surface.withValues(alpha: 0.4),
                    width: double.infinity,
                    height: double.infinity,
                  ),
                ),
              ),
            ),
        ],
      ),
    );
  }
}

