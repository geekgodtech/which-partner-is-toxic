class _DiscountCountdownSnackBar extends StatefulWidget {
  final VoidCallback onUnlock;

  const _DiscountCountdownSnackBar({required this.onUnlock});

  @override
  State<_DiscountCountdownSnackBar> createState() =>
      _DiscountCountdownSnackBarState();
}

class _DiscountCountdownSnackBarState
    extends State<_DiscountCountdownSnackBar> {
  Timer? _timer;
  int _secondsRemaining = 60;

  @override
  void initState() {
    super.initState();
    _timer = Timer.periodic(const Duration(seconds: 1), (timer) {
      if (_secondsRemaining <= 1) {
        timer.cancel();
        if (mounted) {
          setState(() => _secondsRemaining = 0);
        }
        return;
      }

      if (mounted) {
        setState(() => _secondsRemaining -= 1);
      }
    });
  }

  @override
  void dispose() {
    _timer?.cancel();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Row(
      children: [
        Expanded(
          child: Text(
            'Limited demo offer: reveal this full analysis for \$10. Expires in $_secondsRemaining seconds.',
          ),
        ),
        TextButton(
          onPressed: _secondsRemaining > 0 ? widget.onUnlock : null,
          child: const Text('Unlock for \$10'),
        ),
      ],
    );
  }
}

