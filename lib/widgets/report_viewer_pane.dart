class ReportViewerPane extends StatelessWidget {
  const ReportViewerPane({super.key});

  @override
  Widget build(BuildContext context) {
    return ColoredBox(
      color: Theme.of(context).colorScheme.surfaceContainerHighest,
      child: SafeArea(
        child: Consumer<ToxicityAnalyzerController>(
          builder: (context, controller, child) {
            if (controller.isAnalyzing) {
              return const _AnalysisLoadingView();
            }

            final report = controller.activeReport;
            if (report != null) {
              return _AnalysisReportView(
                report: report,
                pdfBytes: controller.activePdfBytes,
                errorMessage: controller.errorMessage,
              );
            }

            return _EmptyReportView(
              statusMessage: controller.statusMessage,
              errorMessage: controller.errorMessage,
              hasActiveThread: controller.activeThread != null,
              selectedMetricCount: controller.selectedMetricCount,
            );
          },
        ),
      ),
    );
  }
}

class _AnalysisLoadingView extends StatefulWidget {
  const _AnalysisLoadingView();

  @override
  State<_AnalysisLoadingView> createState() => _AnalysisLoadingViewState();
}

class _AnalysisLoadingViewState extends State<_AnalysisLoadingView>
    with SingleTickerProviderStateMixin {
  late AnimationController _controller;
  late Animation<double> _fadeAnimation;

  @override
  void initState() {
    super.initState();
    _controller = AnimationController(
      duration: const Duration(milliseconds: 1500),
      vsync: this,
    )..repeat(reverse: true);
    _fadeAnimation = Tween<double>(
      begin: 0.4,
      end: 1.0,
    ).animate(CurvedAnimation(parent: _controller, curve: Curves.easeInOut));
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Center(
      child: FadeTransition(
        opacity: _fadeAnimation,
        child: Container(
          padding: const EdgeInsets.symmetric(horizontal: 48, vertical: 32),
          decoration: BoxDecoration(
            color: Colors.white,
            borderRadius: BorderRadius.circular(16),
